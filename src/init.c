int		init(char *file, t_data *data)
{
	int		i;
	int		fd;
	char	*line;

	fd = open(file, O_RDONLY);
	while (get_next_line(fd, &line) == 1)
	{
		i = 0;
		if (line[0] == 'R')
			init_win(line, data);
		// if ((line[0] =='N' && line[1] == 'O'))
		// if ((line[0] == 'S' && line[1] == 'O'))
		// if ((line[0] == 'W' && line[1] == 'E'))
		// if ((line[0] == 'E' && line[1] == 'A'))
		// if (line[0] == 'S')
		// if (line[0] == 'F')
		// if (line[0] == 'C')
		free(line);
	}
	init_mlx(data);
	init_map(data);
	init_player(data);
	return (1);
}

void	init_mlx(t_data *data)
{
	// data->mlx = malloc(sizeof(t_mlx));
	data->mlx->mlx_ptr = mlx_init();
	data->mlx->win_ptr = mlx_new_window(data->mlx->mlx_ptr, data->win->width, data->win->height, "Cub3D");
}

void	init_map(t_data *data)
{
	int		i;
	int		j;

	i = 0;
	while (i < 24)
	{
		j = 0;
		while (j < 24)
		{
			data->map->map[i][j] = g_map[i][j];
			j++;
		}
		i++;
	}
}

void init_player(t_data *data)
{
	// data->player = malloc(sizeof(t_player));
	data->player->pos.x = 22;
	data->player->pos.y = 12;
	data->player->dir.x = -1;
	data->player->dir.y = 0;
	data->player->plane.x = 0;
	data->player->plane.y = 0.66;
}
void	init_win (char *line, t_data *data)
{
	int		i;

	// data->win = malloc(sizeof(t_win));
	i = 1;
	data->win->width = ft_atoi(&(line[i]));
	i++;
	while (ft_isdigit(line[i]))
		i++;
	data->win->height = ft_atoi(&(line[i]));
}
